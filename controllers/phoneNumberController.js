const PhoneNumber = require("../models/phoneNumberModel");
const PhoneNumberService = require("../services/phoneNumberService");

class PhoneNumberController {
    static async createPhoneNumber(req, res) {
        try {
            const { userID, phoneNumber } = req.body;
            const phoneNumberObj = new PhoneNumber(0, userID, phoneNumber);
            const result = await PhoneNumberService.createPhoneNumber(phoneNumberObj);
            res.status(201).json(result);
        } catch (e) {
            if (process.env.NODE_ENV === 'development') {
                console.error(e.message);
            }
            if (e.statusCode === 404) {
                res.status(e.statusCode).json({ message: 'User does not exist', error: e.message });
            } else {
                res.status(500).json({ message: 'Internal server error', error: e.message });
            }
        }
    }

    static async getPhoneNumber(req, res) {
        try {
            const { phoneNumber } = req.params;
            const result = await PhoneNumberService.getPhoneNumber(phoneNumber);
            res.status(200).json(result);
        } catch (e) {
            if (process.env.NODE_ENV === 'development') {
                console.error(e.message);
            }
            if (e.statusCode === 404) {
                res.status(e.statusCode).json({ message: 'Phone number not found', error: e.message });
            } else {
                res.status(500).json({ message: 'Internal server error', error: e.message });
            }
        }
    }

    static async getPhoneNumberByID(req, res) {
        try {
            const { phoneNumberID } = req.params;
            const result = await PhoneNumberService.getPhoneNumberByID(phoneNumberID);
            res.status(200).json(result);
        } catch (e) {
            if (process.env.NODE_ENV === 'development') {
                console.error(e.message);
            }
            if (e.statusCode === 404) {
                res.status(e.statusCode).json({ message: 'Phone number not found', error: e.message });
            } else {
                res.status(500).json({ message: 'Internal server error', error: e.message });
            }
        }
    }

    static async getAllPhoneNumbers(req, res) {
        try {
            const result = await PhoneNumberService.getAllPhoneNumbers();
            res.status(200).json(result);
        } catch (e) {
            if (process.env.NODE_ENV === 'development') {
                console.error(e.message);
            }
            if (e.statusCode === 404) {
                res.status(e.statusCode).json({ message: 'No phone numbers found', error: e.message });
            } else {
                res.status(500).json({ message: 'Internal server error', error: e.message });
            }
        }
    }

    static async updatePhoneNumber(req, res) {
        try {
            const { phoneNumberID } = req.params;
            const updates = req.body;
            const result = await PhoneNumberService.updatePhoneNumber(phoneNumberID, updates);
            res.status(204).json(result);
        } catch (e) {
            if (process.env.NODE_ENV === 'development') {
                console.error(e.message);
            }
            if (e.statusCode === 404) {
                res.status(e.statusCode).json({ message: 'Phone number not found', error: e.message });
            } else {
                res.status(500).json({ message: 'Internal server error', error: e.message });
            }
        }
    }

    static async deletePhoneNumber(req, res) {
        try {
            const { phoneNumber } = req.params;
            const result = await PhoneNumberService.deletePhoneNumber(phoneNumber);
            res.status(200).json(result);
        } catch (e) {
            if (process.env.NODE_ENV === 'development') {
                console.error(e.message);
            }
            if (e.statusCode === 404) {
                res.status(e.statusCode).json({ message: 'Phone number not found', error: e.message });
            } else {
                res.status(500).json({ message: 'Internal server error', error: e.message });
            }
        }
    }

    static async deleteAllPhoneNumbers(req, res) {
        try {
            const result = await PhoneNumberService.deleteAllPhoneNumbers();
            res.status(200).json(result);
        } catch (e) {
            if (process.env.NODE_ENV === 'development') {
                console.error(e.message);
            }
            if (e.statusCode === 404) {
                res.status(e.statusCode).json({ message: 'No phone numbers found to delete', error: e.message });
            } else {
                res.status(500).json({ message: 'Internal server error', error: e.message });
            }
        }
    }
}

module.exports = PhoneNumberController;