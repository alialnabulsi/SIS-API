const Section = require("../models/sectionModel");
const SectionService = require("../services/sectionService");

class SectionController {
    static async createSection(req, res) {
        try {
            const { courseID, scheduleID, instructorID, finalExamScheduleID, semesterID, sectionCapacity, enrolled, language } = req.body;
            const section = new Section(0, courseID, scheduleID, instructorID, finalExamScheduleID, semesterID, sectionCapacity, enrolled, language);
            const result = await SectionService.createSection(section);
            res.status(201).json(result);
        } catch (e) {
            if (process.env.NODE_ENV === 'development') {
                console.error(e.message);
            }
            if (e.statusCode === 404) {
                res.status(e.statusCode).json({ message: 'One or more foreign keys do not exist', error: e.message });
            } else {
                res.status(500).json({ message: 'Internal server error', error: e.message });
            }
        }
    }

    static async getSection(req, res) {
        try {
            const { sectionID } = req.params;
            const result = await SectionService.getSection(sectionID);
            res.status(200).json(result);
        } catch (e) {
            if (process.env.NODE_ENV === 'development') {
                console.error(e.message);
            }
            if (e.statusCode === 404) {
                res.status(e.statusCode).json({ message: 'Section not found', error: e.message });
            } else {
                res.status(500).json({ message: 'Internal server error', error: e.message });
            }
        }
    }

    static async getAllSections(req, res) {
        try {
            const result = await SectionService.getAllSections();
            res.status(200).json(result);
        } catch (e) {
            if (process.env.NODE_ENV === 'development') {
                console.error(e.message);
            }
            if (e.statusCode === 404) {
                res.status(e.statusCode).json({ message: 'No sections found', error: e.message });
            } else {
                res.status(500).json({ message: 'Internal server error', error: e.message });
            }
        }
    }

    static async updateSection(req, res) {
        try {
            const { sectionID } = req.params;
            const updates = req.body;
            const result = await SectionService.updateSection(sectionID, updates);
            res.status(204).json(result);
        } catch (e) {
            if (process.env.NODE_ENV === 'development') {
                console.error(e.message);
            }
            if (e.statusCode === 404) {
                res.status(e.statusCode).json({ message: 'Section not found', error: e.message });
            } else {
                res.status(500).json({ message: 'Internal server error', error: e.message });
            }
        }
    }

    static async deleteSection(req, res) {
        try {
            const { sectionID } = req.params;
            const result = await SectionService.deleteSection(sectionID);
            res.status(200).json(result);
        } catch (e) {
            if (process.env.NODE_ENV === 'development') {
                console.error(e.message);
            }
            if (e.statusCode === 404) {
                res.status(e.statusCode).json({ message: 'Section not found', error: e.message });
            } else {
                res.status(500).json({ message: 'Internal server error', error: e.message });
            }
        }
    }

    static async deleteAllSections(req, res) {
        try {
            const result = await SectionService.deleteAllSections();
            res.status(200).json(result);
        } catch (e) {
            if (process.env.NODE_ENV === 'development') {
                console.error(e.message);
            }
            if (e.statusCode === 404) {
                res.status(e.statusCode).json({ message: 'No sections found to delete', error: e.message });
            } else {
                res.status(500).json({ message: 'Internal server error', error: e.message });
            }
        }
    }
}

module.exports = SectionController;